package org.apache.camel.learn.Datos;

public class Facturado {
    private String ID;
    private int Mes;
    private int Valor;

    public String getID() {
        return ID;
    }
    public void setID(String iD) {
        ID = iD;
    }
    public int getMes() {
        return Mes;
    }
    public void setMes(int mes) {
        Mes = mes;
    }
    public int getValor() {
        return Valor;
    }
    public void setValor(int valor) {
        Valor = valor;
    }

    
}
